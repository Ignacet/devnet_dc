""" Funcion suma: (int,int) -> int
    uso: suma(2,3)

    Funcion resta: (int,int) -> int
    uso: resta(3,2)
"""

def suma (_val1,_val2):
    return _val1+_val2

def resta(_val1,_val2):
    return _val1-_val2


def main():
    print ("suma: ",suma(3,3))
    print ("resta: ",resta(3,2))

if __name__ == "__main__":
    main()

